package ex02연산자;

public class ex05증감연산자 {
	public static void main(String[] args) {
		int num = 10;
		System.out.println(num++);
		System.out.println(num);
		
		int num2 = 10;
		System.out.println(num2--);
		
		int num2a = 10;
		System.out.println(--num2a);
		
		int opr =0;
		opr += 3;
		System.out.println(opr++);
		System.out.println(opr);
		System.out.println(++opr);
		System.out.println(opr);
		System.out.println(opr--);
		System.out.println(opr);
		System.out.println(--opr);
		System.out.println(opr);
		
	}
}
